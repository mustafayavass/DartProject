class matematik {
  static const pi = 3.14;

  static topla(double sayi1, double sayi2) {
    return sayi1 + sayi2;
  }

  static cikar(double sayi1, double sayi2) {
    return sayi1 - sayi2;
  }

  static carp(double sayi1, double sayi2) {
    return sayi1 * sayi2;
  }

  static bol(double sayi1, double sayi2) {
    return sayi1 / sayi2;
  }
}
